<h3>Go Electronic; Save Money and Paper!</h3>
Maidu Group is updating our list of members who have said they would like to receive Maidu Messages electronically, rather than on paper. Increasing the number of electronic subscribers will save us money for printing and postage. Well over half of our annual budget goes to these costs.
Our first concern is that you will read our newsletter in which- ever form you choose to receive it. We plan to send four quarterly newsletters this year. If you would prefer receiving these newsletters electronically, please send your email address to us at twoperegrines@yahoo.com. If you prefer receiving the paper version of the newsletter, you don't need to do anything at this time. In either case, we hope you will read Maidu Messages and become involved to whatever extent possible.
<br><br>
Maidu Group also maintains contact lists for Events and Alerts. The Events list is used to inform members by email of upcoming events such as general meetings and events involving other groups which you might find to be of interest. The Alerts list is used to inform members by email of critical conservation is- sues. The Alerts list may be used to ask members to write a letter, contact elected officials or attend meetings about urgent environmental issues If you would like to be on either of these lists, please send your email address to twoperegrines@yahoo.com and indicate which list(s) you would like to be on.

