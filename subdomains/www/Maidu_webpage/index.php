<h3> From the Forest Service!  Remember proper disposal of ashes!</h3>
Do follow proper procedures for discarding woodstove and fireplace ashes.  The U.S. Forest Service Lake Tahoe Basin Management Unit (LTBMU) has already responded to two wildfires caused by disposal of woodstove ashes on National Forest System lands this recent Autumn.  Residents were found to have dumped their woodstove ashes onto National Forest lands just beyond their property boundary.  Neither house of involved residents had defensible space, and the resulting wildfires could have destroyed the homes or adjacent structures.  The residents can be cited for "negligently placing any material that could start a fire," which carries a $350 fine.  Residents can also be held responsible for the costs of fire suppression.  The weather change doesn't mean we can stop paying attention to fire prevention.  "Woodstove ashes can retain enough heat to start a fire in your home or surrounding forest for many days after you think your fire's out," says Beth Brady, LTBMU fire prevention officer.  So, what to do with your ashes?  Place ashes in a metal container, which should not be stored in the house or garage, or on any flammable surface, such as a deck.  The ashes should cool for at least four days before final disposal, and any hot spots doused with water.
