<h3>El Dorado and Amador Counties' outdoor interests</h3>

Hi, all Maidu Group enthusiasts!  Take a moment to note the following updates in information relating to El Dorado and Amador Counties' outdoor interests.
<ul>

<li><b>Fallen Leaf Lake Trail Access and Management Project</b>
"Do You Visit Fallen Leaf Lake?"
<br><br>
The Forest Service seeks your public comment on the proposed Fallen Leaf Lake Trail Access and Management Project, covering about 12,960 acres on the South Shore of Lake Tahoe.
<br><br>
Extending from Pope to Baldwin beaches on the north end, to the south and west ends of Fallen Leaf Lake, the Lake Tahoe Basin Management Unit's (LTBMU) project proposes to expand and improve public parking and access.  The access road to Glen Alpine Trailhead would be upgraded, and an emergency access road would be constructed at the northeast corner of Fallen Leaf Campground.  In addition, over forty miles of trails in the project area would be improved in order to meet current Forest Service standards, while others are proposed to be decommissioned.  The existing bridge over Fallen Leaf Lake dam would be removed and replaced with an accessible bridge designed to accommodate bicycle and equestrian use.
<br><br>
Project work may begin as early as Spring of 2012, and completed by 2014.  During the implementation of the project, visitors may encounter temporary area closures or restrictions, as well some noise and visual impacts in the campground and beach areas.
<br><br>
If you can, make your comments known by the close of the day by Wednesday, November 30th.  You will find a detailed description of all proposed trail and parking activities at the website:  fs.usda.gov/gotolltbmuIFallenLeafLakeATM, or contact Jacob Quinn at 530-543-2609.  You may also email J. Quinn at jmquinn@fs.fed.us.
</li>

<li><b>Upper Echo Lakes Hazardous Fuels Reduction project</b>The U.S. Forest Service - Lake Tahoe Basin Management Unit (LTBMU) - seeks your input on the Upper Echo Lakes Hazardous Fuels Reduction project.  The project includes tree thinning and surface fuels reduction that is intended to reduce the risk of wildfire and improve fire fighting effectiveness.  Located on about 100 acres at Upper Echo Lake in El Dorado County within the wildland urban interface, the project would ensure public safety while being implemented.  Brief closures of areas adjacent to the Pacific Crest Trail are possible while  the project's design  design includes resource protection measures to maintain the scenic nature of the trail.  The proposed treatments would establish a defense zone around seasonal residence cabins, allowing firefighters to carry out effective fire suppression activities in the event of a wildfire.
<br><br>
	The project's work would consist of cutting and hand-piling brush and conifer trees up to 16 inches in diameter; vegetation would be cut by hand/chain saws/other tools, with resulting debris piles to be burned one to three years later.  Work would begin as early as this Autumn and may take up to six years to complete.
<br><br>
The public comment period ends August 15th.  A map is available for your review at the LTBMU Forest Supervisor's office:  35 College Drive, South Lake Tahoe - 96150, or on-line at fs.usda.gov/goto/ltbmu/UpperEchoLakesFuelsReduction</li>

	
	
<li> <b>Desolation Wilderness permits now available to the public online!</b>  A more convenient way for us to get our permits is now available:  recreation.gov.  Now we can make our reservations any time of the day, seven days a week.  A call center will still exist to assist with the reservation process, but no longer accept permit reservations over the 'phone, through the mail or by fax.  Reserved permits will also no longer be mailed.  Forest Service staff at the Pacific Ranger Station in Fresh Pond and the Lake Tahoe Basin Management Unit will still be available to assist visitor with local information and wilderness trip planning.  More information about Desolation Wilderness permits is available at the following websites:  fs.fed.us/r5/eldorado, and fs.fed.us/r5/ltbm.  Get out this upcoming season!  Hike, peak bag, explore our public lands!

</li>

<li> <b>Our own Hazel Creek Campground at Jenkinson Lake (Sly Park)</b> is a lucky recipient of over $150,000 ear tagged for work designed to protect Hazel Creek and Jenkinson Lake, as well restore the campground.  Restoration designs and permit applications will be created to stabilize creek beds and to build a foot bridge.  These monies are thanks to the Sierra Nevada Conservancy (SNC) Board who have okay'ed $10 million (from the 2006 Safe Drinking Water Bond of Proposition 84) in watershed protection projects in the Sierra.  According to the SNC Executive Officer Jim Branham, "These projects will provide jobs and a variety of benefits to the Sierra Nevada region, including reducing the risk of large, damaging fires; restoring meadows; protecting habitat; supporting working ranches; and improving water quality."
