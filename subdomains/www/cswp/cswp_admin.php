<h3>CSWP Quarterly Meeting Agenda and Notes</h3>

May 10, 2012, Sierra Club Headquarters Sacramento<br><br>
by Lee Miller
<br><br>
*Indicates action item for attendees<br><br>
Attendees: Karen, Virginia, Chuck, Milt, Lee, Evan.<br><br>

1a. Milt brought up the issue of providing pens with our name and a slogan for gifts at
the roulette wheel when used at table events. 
<br><br>*Milt will check on prices and we will get 1000 if feasible.
<br><br>
1b. Spanish version of Population brochure. There is $200 + dollars left in our account
with which to print this brochure. Some discussion of members printing them personally
on an ad hoc basis, but it would be more efficient to print a large batch with money
left in the till. 
<br><br>*Chuck will check on the amount in our account and 
<br><br>*Elaine will check
on prices to print it. 
<br><br>We also discussed the need to propose budget coverage of future
reprint and other expenses such as bumper stickers, pens etc. Budget proposals need
to be prepared at budget time in Jan-Feb. This issue needs to be addressed before
year's end.
<br><br>
1c. Virginia brought up the issue of vasectomy as a method that has been somewhat
neglected and encouraged us to make people aware of its benefits-freedom from worry
about contraceptive failures and a better sex life.
<br><br>
1. Michael Brune’s posted comment on global population (Kimberly Lovell)
<br><br>
2. Funding for GPEP...how to keep heat on? Evan suggested we write a letter
to the National Board advocating more funding for GPEP subject to proper
procedures, protocols. 
<br><br>
*Evan will draft a letter.
<br><br>
3. Recent screenings of Mother...Placer Group SC, ASRA, ECOS fair (Sierra
College). We need a dedicated projection system!!!
Evan expressed frustration at having equipment problems at some screenings. If
we had our own laptop, speakers and projector then the event could always go
on. *Milt will check on costs for a projector and Lee, Evan and Milt expressed
willingness to privately fund this equipment purchase. A laptop may be borrowed
to get by the next event. Milt provided a speaker system that is battery driven and
plugs into a laptop.<br><br>
*Karen is checking on the laptop which may be a gift, not a loaner
<br><br>
4. Ooops..missed 2012 Sac City Earth Day fair...next year!
<br><br>
5. Population article in current Sierra Magazine:<br>
The article was not constructed from a science viewpoint, but at least the
organization demonstrated some awareness of the population issue which was
welcomed by all. *Evan wrote a LTE and encouraged others to write with praise
and critiques combined as appropriate.
<br><br>
6. Recent purchases...slogans(Make Love Not Babies, Overpopulation is
Sexually Transmitted...other possibilities include Breed Responsibly, World
Population...Size Matters, World's Fate is in your Hands and in Your Pants,
Family Planning...a Good Plan, Too Many Pops = Overpopulation), roulette wheel (prizes?), metronome(ticktick)
Evan demonstrated our roulette wheel, the metronome (awesome noisy fast rate
of additions to our planet) and the prizes awarded for playing the roulette wheel.
It does attract the folks to our booth.
<br><br>
7. Report...meeting with Kimberly Lovell, 2/27, SF.<br>
The group that met with her was all impressed with her talent. Funding problems
are related to the withdrawal of foundation support not lack of board support.
<br><br>
8. We met with Kathryn Phillips, Sierra Club's Legislative Director, briefly and she
explained how our lobbying efforts have been curtailed due to budget restraints.
Much time is spent fighting bad bills. Supporting bills on family planning and
abortion as introduced by PP may be a problem for both organizations, so we
need to tread carefully so as not to get branded as racist which seems too often
to be the strategy for those disagreeing with such efforts.
<br><br>
9. Upcoming events...Whole Earth Festival, UCD, May 11,12,13...Stone Lakes
WLP, May 19,... PARC festival June 10
<br><br>
10. UCD ad...”Overpopulation is Sexually Transmitted” runs through June 10.
Evan reported that this Personals ad is very cheap to run and is bound to get
attention. However, other schools have much higher rates for ads, so this is a
somewhat unique situation.
<br><br>
11. Deadline for CSWP Bonanza article - May 16th.
<br><br>
*Lee agreed to do the write up based on our meeting, events and any other items
committee members submit. Virginia stressed she would like to see mention of
vasectomy as a bc method.
<br><br>
12. CNRCC Population Strategies
Karen has submitted strategies as well as support for PP bills on sex education
and family planning. She will try to get more activists involved, and more exposure via chapter newsletters and the CNRCC website, and a semi-annual population report, as well as using CSWP as a good example of how an activist organization can be successful.
<br><br>
13. CSWP Webpage Update (add Alert Section) Karen wants to make the
website calendar available for updates from members when she is not available.
Members will access calendar with a password. 
<br><br>
*Elaine said she would be willing to attempt to do this and would help with Evan's event listings.
<br><br>
14. Selection of a new CSWP Chair - It was decided by unanimous acclaim that
Chuck would remain as chair with help volunteered from all to get things done.
<br><br>
* Karen would serve as Vice-Chair in Chuck's absence or as needed. Tentative
date for Stockton meeting at Lee’s home is Monday, August 20th.
<br><br>
* Elaine volunteered to do the agenda as needed
<br><br>
* Notify Motherlode Conservation committee with addition of Karen as VP.
